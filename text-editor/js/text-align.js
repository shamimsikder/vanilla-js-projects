// Function for Left Text Align
function leftTextAlign(){

    const text = document.getElementById('input-field')

    text.style.textAlign = 'left'

}

// Function for Center Text Align
function centerTextAlign(){

    const text = document.getElementById('input-field')

    text.style.textAlign = 'center'

}

// Function for Right Text Align
function rightTextAlign(){

    const text = document.getElementById('input-field')

    text.style.textAlign = 'right'

}