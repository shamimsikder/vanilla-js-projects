// Function for Bold Text
function boldFontStyle(){

    const text = document.getElementById('input-field')

    text.style.fontWeight = 'bold'

}

// Function for Italic Text
function italicFontStyle(){

    const text = document.getElementById('input-field')

    text.style.fontStyle = 'italic'

}

// Function for Text Underline
function underlineFontStyle(){

    const text = document.getElementById('input-field')

    text.style.textDecoration = 'underline'

}

